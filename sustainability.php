<script>
    $(document).ready(function () {
        $('.btns.lft').on('click', function () {
            $('.grid')
                .removeClass('right-active')
                .addClass('left-active');
        });
        $('.btns.rgt').on('click', function () {
            $('.grid')
                .removeClass('left-active')
                .addClass('right-active');
        });
        $('.back-btn').on('click', function () {
            $('.grid').removeClass('left-active right-active');
        });
    });
</script>

<!-- Collapse -->
<script>
    $(document).on('click touchstart', function (e) {
        if (
            !$(e.target).closest('.hightlight-bx').length &&
            !$(e.target).closest('[data-bs-toggle="collapse"]').length
        ) {
            $('.collapse.show').collapse('hide');
        }
    });
</script>

