<!-- Filter -->
<script>
    $(document).ready(function () {
        const items = $('.news_listing .row > div[class*="col"]');
        const loadMoreBtn = $('#loadMoreBtn');
        const loadLessBtn = $('#loadLessBtn');
        let currentFilter = 'all';
        const getDefaultCount = () =>
            window.innerWidth >= 1200 ? 6 :
            window.innerWidth >= 768 ? 6 :
            window.innerWidth >= 576 ? 4 : 4;
        let defaultCount = getDefaultCount();
        let visibleCount = defaultCount;
        function updateItems() {
            let filteredItems;
            if (currentFilter === 'all') {
                filteredItems = items;
            } else {
                filteredItems = items.filter(function () {
                    return $(this).attr('data-category').includes(currentFilter);
                });
            }
            items.hide();
            filteredItems.each(function (index) {
                if (index < visibleCount) {
                    $(this).show();
                }
            });
            loadMoreBtn.toggle(filteredItems.length > visibleCount);
            loadLessBtn.toggle(visibleCount > defaultCount);
        }
        $('.filter-btns button').on('click', function () {
            currentFilter = $(this).data('filter');
            $(this).addClass('active')
                .siblings()
                .removeClass('active');
            this.scrollIntoView({
                behavior: 'smooth',
                inline: 'center',
                block: 'nearest'
            });
            visibleCount = defaultCount;
            updateItems();
        });
        loadMoreBtn.on('click', function () {
            let filteredItems;
            if (currentFilter === 'all') {
                filteredItems = items;
            } else {
                filteredItems = items.filter(function () {
                    return $(this).attr('data-category').includes(currentFilter);
                });
            }
            visibleCount = filteredItems.length;
            updateItems();
        });
        loadLessBtn.on('click', function () {
            visibleCount = defaultCount;
            updateItems();
        });
        $(window).on('resize', function () {
            let newDefault = getDefaultCount();
            if (newDefault !== defaultCount) {
                defaultCount = newDefault;
                visibleCount = defaultCount;
                updateItems();
            }
        });
        updateItems();
    });
</script>

