document.addEventListener('DOMContentLoaded', function() {
    const navTitles = document.querySelectorAll('.navTitle');
    function handleNavClick() {
        if (window.innerWidth <= 767) {
            const ftrLink = this.nextElementSibling;
            if (ftrLink.style.display === 'block') {
                ftrLink.style.display = 'none';
                this.classList.remove('active');
            } else {
                document.querySelectorAll('.ftr_wrp').forEach(link => {
                    link.style.display = 'none';
                });
                navTitles.forEach(t => t.classList.remove('active'));
                ftrLink.style.display = 'block';
                this.classList.add('active');
            }
        }
    }
    navTitles.forEach(title => {
        title.addEventListener('click', handleNavClick);
    });
    window.addEventListener('resize', function() {
        if (window.innerWidth > 767) {
            document.querySelectorAll('.ftr_wrp').forEach(link => {
                link.style.display = '';
            });
            navTitles.forEach(t => t.classList.remove('active'));
        }
    });
});

